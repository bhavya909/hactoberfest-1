#include<stdio.h>
#include<math.h>
struct Stack
{
    int item[10];
    int Top;
};
/*******************************************************************************/
struct Stack S;
/*******************************************************************************/
void initialise(void)
{
    S.Top=-1;
}
/*******************************************************************************/
void push(int x)
{
    S.Top++;
    S.item[S.Top]=x;
}
/*******************************************************************************/
int pop()
{
    if (S.Top==-1)
    {
        return ;
    }
    else
    {
        int m;
        m=S.item[S.Top];
        S.Top--;
        return m;
    }

}
